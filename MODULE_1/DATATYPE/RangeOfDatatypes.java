public class RangeOfDatatypes
{
	public static void main(String[] args) 
	{
		System.out.println(".....Range of Datatypes.....");

		System.out.println("for byte..");
		System.out.println("MIN VALUE : " + Byte.MIN_VALUE);
		System.out.println("MAX VALUE : " + Byte.MAX_VALUE);

		System.out.println("for boolean..");
		System.out.println("MIN VALUE : " + Boolean.TRUE);
		System.out.println("MAX VALUE : " + Boolean.FALSE);
		
	}
}